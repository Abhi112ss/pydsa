def solve(n: int, k: int) -> int:
    """
    Finds the k-th lucky number using binary search on the value range.
    
    A lucky number is defined by a process of elimination. However, the problem 
    can be rephrased: we want to find the smallest integer 'x' such that the 
    count of lucky numbers less than or equal to 'x' is at least 'k'.
    
    The elimination process:
    1. Start with all positive integers.
    2. Remove every 2nd number.
    3. Remove every 3rd number from the remaining.
    4. Remove every 4th number from the remaining, and so on.
    
    Wait, the standard "Lucky Number" definition (Josephus-style or sieve-style) 
    is different from the specific LeetCode 2802 definition. 
    LeetCode 2802 defines the sequence as:
    - The first number is 1.
    - The next numbers are generated by a specific rule.
    Actually, looking at the problem constraints and definition:
    The sequence is: 1, 3, 5, 7, 9... (all odd numbers) is NOT it.
    The problem states: "The k-th lucky number is the k-th number in the sequence 
    formed by removing elements."
    
    Actually, the problem 2802 is:
    A number is lucky if it is not removed.
    The removal process:
    - Step 1: Remove every 2nd number.
    - Step 2: Remove every 3rd number from the remaining.
    - Step 3: Remove every 4th number from the remaining.
    ... and so on.
    
    Args:
        n (int): The upper bound of the range (though k is the primary driver).
        k (int): The index of the lucky number to find.

    Returns:
        int: The k-th lucky number.

    Examples:
        >>> solve(10, 3)
        5
        >>> solve(100, 10)
        19
    """

    def count_lucky_up_to(x: int) -> int:
        """
        Counts how many lucky numbers exist in the range [1, x].
        Uses the principle of inclusion-exclusion/sieve logic.
        """
        # current_count represents how many numbers are left in the sequence
        # after several rounds of elimination.
        current_count = x
        
        # We simulate the sieve. In each step 'i', we remove every i-th element.
        # However, the 'i' refers to the position in the *current* remaining list.
        # This is equivalent to saying: if we have 'current_count' elements,
        # the number of elements removed in this step is current_count // i.
        
        # The sequence of divisors starts from 2, 3, 4...
        # But we only care about divisors that are actually "lucky" themselves.
        # Wait, the problem says: "remove every 2nd, then every 3rd, then every 4th..."
        # This is exactly the Sieve of Flavius Josephus.
        
        # Let's use the property: if we want to find how many numbers <= x 
        # survive the sieve up to step 'm'.
        # This is a known problem. The number of elements remaining after 
        # removing every 2nd, then every 3rd, etc.
        
        # Actually, the problem 2802 is simpler: 
        # The k-th lucky number is the k-th number that survives the sieve.
        # The sieve steps are: 2, 3, 4, 5...
        
        # Let's re-evaluate:
        # To find how many numbers <= x survive:
        # Let f(x, step) be the count of numbers <= x surviving after 'step' rounds.
        # f(x, 1) = x - x // 2
        # f(x, 2) = f(x, 1) - f(x, 1) // 3
        # ...
        # This is not quite right because the 'step' is the index in the *new* list.
        
        # Correct approach for "Lucky Numbers" (Sieve of Flavius):
        # A number 'x' survives if it is not removed.
        # Instead of counting how many survive, let's track the position of 'x'.
        # If we want to know if 'x' is the k-th lucky number, we can track its 
        # position through the rounds.
        
        # But the problem asks for the k-th lucky number.
        # Let's use the inverse: If we want the k-th number, we can find its 
        # original value.
        # Let's use the property: if a number is at position 'pos' in the current 
        # list, its position in the previous list was:
        # pos_prev = pos + (pos - 1) // (step - 1)
        # This is for finding the original index of the k-th element.
        
        # Let's use the position-tracking method:
        # To find the k-th lucky number:
        # Start with pos = k.
        # We need to reverse the process.
        # In the last step (say step m), the k-th element was at some position.
        # This is tricky because we don't know when to stop.
        
        # Let's use the "count_lucky_up_to" logic correctly.
        # A number 'x' is lucky if it survives all steps.
        # The steps are: 2, 3, 4, ...
        # But the step 'i' only happens if 'i' is a lucky number.
        # Wait, the problem says: "remove every 2nd, then every 3rd, then every 4th..."
        # This means the step is just the integer 2, 3, 4... regardless of whether 
        # the step itself was lucky.
        
        # Let's re-read: "In the first step, remove every 2nd number. 
        # In the second step, remove every 3rd number from the remaining..."
        # This is exactly:
        # Step 1: i=2. Remove elements at indices 2, 4, 6... (1-indexed)
        # Step 2: i=3. Remove elements at indices 3, 6, 9... (1-indexed)
        
        # Let's use the position transformation:
        # If an element is at position 'p' after step 'i', its position 
        # before step 'i' was:
        # p_prev = p + (p - 1) // (i - 1) is WRONG.
        # If we remove every i-th element, the element at position 'p' 
        # was at position 'p_prev' such that p_prev - (p_prev // i) = p.
        
        # Let's use the binary search on the answer 'x'.
        # How many numbers <= x survive?
        # Let's use a recursive/iterative function:
        # count(x, step):
        #   if step > x: return x
        #   return count(x - x // step, step + 1)
        # This is still not quite right.
        
        # Let's use the property:
        # The k-th lucky number can be found by starting with pos = k
        # and working backwards.
        # But we don't know the number of steps.
        # Actually, the number of steps is limited. If the step > current_pos, 
        # no more elements will be removed at or before this position.
        
        # Let's use the "position of k" approach:
        # We want to find 'x' such that after all removals, its position is 'k'.
        # This is equivalent to:
        # Start with pos = k.
        # For step = 2, 3, 4...
        # We need to find the position in the previous step.
        # If we are at position 'p' in the current list, what was the position 
        # in the previous list?
        # The elements removed were at indices i, 2i, 3i...
        # The element at position 'p' in the current list was at position 'p_prev'
        # in the previous list, where p_prev - floor(p_prev / i) = p.
        # We can solve for p_prev:
        # p_prev = p + floor((p - 1) / (i - 1)) is also not quite right.
        # Let's test: i=2. p=1 -> p_prev=1. p=2 -> p_prev=3. p=3 -> p_prev=5.
        # Formula: p_prev = p + (p - 1) // (i - 1) ? 
        # If i=2: p=1 -> 1 + 0 = 1. p=2 -> 2 + 1 = 3. p=3 -> 3 + 2 = 5. Correct.
